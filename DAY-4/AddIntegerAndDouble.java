//Write a program to add an integer variable having value 5 and a double variable having value 6.2.
public class AddIntegerAndDouble {
    public static void main(String[] args) {
        // Declare and initialize the integer and double variables
        int integerValue = 5;
        double doubleValue = 6.2;
        
        // Add the integer and double values
        double result = integerValue + doubleValue;
        
        // Print the result
        System.out.println("The sum of " + integerValue + " and " + doubleValue + " is: " + result);
    }
}